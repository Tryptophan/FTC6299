task flipperFlapper() {
	while (true) {
		while (joy1Btn(07) == 1) {
			motor[flip] = 25;
		}
		while (joy1Btn(08) == 1) {
			motor[flip] = -25;
		}

		motor[flip] = 0;
		wait10Msec(5);
	}
}
task driveControl() {
	bool slowmo = false;
	while (true) {
		getJoystickSettings(joystick);
		if ((abs(joystick.joy1_y1) > 10 || abs(joystick.joy1_y2) > 10) && !slowmo) {
				motor[motorFL] = joystick.joy1_y1 / 1.28;
				motor[motorBL] = joystick.joy1_y1 / 1.28;
				motor[motorFR] = joystick.joy1_y2 / 1.28;
				motor[motorBR] = joystick.joy1_y2 / 1.28;
		}
		else if ((abs(joystick.joy1_y1) > 10 || abs(joystick.joy1_y2) > 10) && slowmo) {
				motor[motorFL] = joystick.joy1_y1 / 4.28;
				motor[motorBL] = joystick.joy1_y1 / 4.28;
				motor[motorFR] = joystick.joy1_y2 / 4.28;
				motor[motorBR] = joystick.joy1_y2 / 4.28;
		}
		else {
			motor[motorFL] = 0;
			motor[motorBL] = 0;
			motor[motorFR] = 0;
			motor[motorBR] = 0;
			while (joy1Btn(02) == 1) {
				float threshold = 0.8;
				int power = 80;
				displayCenteredBigTextLine(3, "%2i", heading);
				heading += valInRange(HTGYROreadRot(SENSOR_GYRO), threshold) * (float)(20 / 1000.0);

				// Checks if the gyro is outside of the specified threshold (1.0)
				if (isInRange(heading, 0, threshold)) {
					setMotors(power, power);
				}

				// If not, lower the speed of the required side of the robot to adjust back to 0
				else {
					if (heading > 0) {
						setMotors((power / 4), power);
					}
					if (heading < 0) {
						setMotors(power, (power / 4));
					}
				}
				wait1Msec(20);
			}
		}

		if (joystick.joy1_TopHat == 0) {
			slowmo = false;
		}
		if (joystick.joy1_TopHat == 4) {
			slowmo = true;
		}
	}
}

task servos() {
	while (true) {
		if (joy1Btn(05) == 1) {
			servo[servoL] = 250;
			servo[servoR] = 0;
		}
		if (joy1Btn(06) == 1) {
			servo[servoL] = 160;
			servo[servoR] = 90;
		}

		// BTN01 = X
		if (joy2Btn(01) == 1) {
			servo[liftServoL] = 100;
			servo[liftServoR] = 155;
		}

		// BTN04 = Y
		if (joy2Btn(04) == 1) {
			servo[liftServoL] = 230;
			servo[liftServoR] = 25;
		}
	}
}

task slide()
{
	while(true){
		while(abs(joystick.joy2_y2) >= 10){
			motor[liftL] = joystick.joy2_y2 / 1.28;
			motor[liftR] = joystick.joy2_y2 / 1.28;
		}
		motor[liftL] = 0;
		motor[liftR] = 0;
	}
}
task main () {
	waitForStart();
	wait1Msec(50);
	HTGYROstartCal(SENSOR_GYRO);
	heading = 0;
	wait1Msec(50);
	startTask(driveControl);
	startTask(servos);
	startTask(flipperFlapper);
	startTask(slide);
	while(true){}
}
